// Funci√≥n para activar AR Simple (sin marcador) - ¬°M√ÅS F√ÅCIL!
function activarARSimple() {
    const arContainer = document.getElementById('ar-container-simple');
    const arButtonContainer = document.getElementById('ar-button-container');
    
    if (!arContainer || !arButtonContainer) {
        alert('‚ùå Error: Elementos AR no encontrados');
        return;
    }
    
    // Verificar soporte de c√°mara
    if (!navigator.mediaDevices || !navigator.mediaDevices.getUserMedia) {
        alert('‚ùå Tu navegador no soporta acceso a la c√°mara.\n\n‚úÖ Por favor usa:\n- Chrome (Android/PC)\n- Safari (iPhone/iPad)\n- Firefox (Android/PC)');
        return;
    }
    
    // Solicitar acceso a la c√°mara
    navigator.mediaDevices.getUserMedia({ 
        video: { 
            facingMode: 'environment', // C√°mara trasera en m√≥viles
            width: { ideal: 1280 },
            height: { ideal: 720 }
        } 
    })
    .then(function(stream) {
        console.log('‚úÖ C√°mara activada correctamente');
        
        // Detener el stream de prueba
        stream.getTracks().forEach(track => track.stop());
        
        // Mostrar el contenedor AR
        arContainer.style.display = 'block';
        arButtonContainer.style.display = 'none';
        document.body.style.overflow = 'hidden';
        
        // Esperar a que A-Frame inicialice
        setTimeout(() => {
            const scene = arContainer.querySelector('a-scene');
            if (scene) {
                console.log('‚úÖ Escena A-Frame cargada');
                
                // Forzar renderizado
                scene.renderer.setPixelRatio(window.devicePixelRatio);
                
                // Mostrar mensaje de √©xito
                setTimeout(() => {
                    console.log('üéâ AR listo! Pikachu deber√≠a estar visible');
                }, 1000);
            }
        }, 500);
        
        // Mensaje de ayuda
        console.log('üí° Tip: Mueve tu dispositivo lentamente para ver a Pikachu');
    })
    .catch(function(error) {
        console.error('‚ùå Error al acceder a la c√°mara:', error);
        
        let mensaje = '‚ùå No se pudo acceder a la c√°mara.\n\n';
        
        if (error.name === 'NotAllowedError' || error.name === 'PermissionDeniedError') {
            mensaje += 'ÔøΩ Permiso denegado.\n\n';
            mensaje += 'Por favor:\n';
            mensaje += '1. Permite el acceso a la c√°mara\n';
            mensaje += '2. Recarga la p√°gina\n';
            mensaje += '3. Intenta de nuevo';
        } else if (error.name === 'NotFoundError') {
            mensaje += 'üì∑ No se encontr√≥ c√°mara.\n\n';
            mensaje += 'Verifica que tu dispositivo tenga c√°mara.';
        } else if (error.name === 'NotReadableError') {
            mensaje += '‚ö†Ô∏è C√°mara en uso.\n\n';
            mensaje += 'Cierra otras apps que usen la c√°mara.';
        } else {
            mensaje += 'Error: ' + error.message;
        }
        
        alert(mensaje);
    });
}

